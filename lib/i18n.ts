export const translations: Record<Lang, typeof en> = { en, ar };

export function useT() {
  const lang = useLanguageStore((s) => s.lang);
  return translations[lang];
}

export function getItemName(
  item: { name: string; translations?: Record<string, { name: string; description?: string }> },
  lang: Lang
): string {
  return item.translations?.[lang]?.name ?? item.name;
}

export function getItemDescription(
  item: { description?: string; translations?: Record<string, { name: string; description?: string }> },
  lang: Lang
): string | undefined {
  return item.translations?.[lang]?.description ?? item.description;
}
